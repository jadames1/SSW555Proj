
def getLast(name):
	theIndex = name.index("/")
	return name[theIndex:]

def checkSameLastNames(fam, ind):
	for f in fam:
		if "HUSB" in fam[f]:
			hus = fam[f]["HUSB"]
			lastName = getLast(ind[hus]["name"])
		else:
			lastName = ""
		if "CHIL" in fam[f] :
			for c in fam[f]["CHIL"]:
				childLast = getLast(ind[c]["name"])
				gender = ind[c]["sex"]
				if gender == "M" and childLast != lastName:
					print("Incorrect")
					fam[f]["CHIL"].pop(fam[f]["CHIL"].index(c))
					ind.pop(c, None)
				else:
					print("correct")
		

